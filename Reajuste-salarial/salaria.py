# Solicita ao usuário que insira o salário atual
salario_atual = float(input("Digite o salário atual do funcionário: "))

# Calcula o novo salário com um aumento de 15%
novo_salario = salario_atual * 1.15

# Mostra o novo salário
print(f"O novo salário do funcionário, com 15% de aumento, é:, {novo_salario:.3f}")

